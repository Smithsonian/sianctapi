CREATE TABLE observations (
  observation_id INT NOT NULL AUTO_INCREMENT COMMENT 'Autogenerated numeric ID that is unique for each observation (using MySQL AUTO_INCREMENT feature) ',
  sequence_id VARCHAR(45) NOT NULL COMMENT 'PID for specific sequence in Database',
  sidora_deployment_id VARCHAR(45) NULL COMMENT 'PID for parent deployment',
  begin_time VARCHAR(45) NOT NULL COMMENT 'Start time of sequence',
  end_time VARCHAR(45) NOT NULL COMMENT 'End time of sequence',
  iucn_id INT NOT NULL COMMENT 'IUCN ID for animal IDed in sequence',
  age VARCHAR(45) NOT NULL COMMENT 'Age of animal identified in sequence',
  sex VARCHAR(45) NOT NULL COMMENT 'Sex of animal IDed in sequence',
  individual VARCHAR(45) NOT NULL COMMENT 'Individually identifiable or not, aka does it have any identifying features in the photo sequence, like stripes or tags.',
  count VARCHAR(45) NOT NULL COMMENT 'Number of animals featured in sequence',
  id_type VARCHAR(45) NOT NULL COMMENT 'How sequence was IDâ€™ed',
  PRIMARY KEY (observation_id),
  FOREIGN KEY (sidora_deployment_id) REFERENCES deployments (sidora_deployment_id),
  FOREIGN KEY (iucn_id) REFERENCES species (iucn_id))
